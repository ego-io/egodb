<script lang="ts">
  import * as Sheet from "$lib/components/ui/sheet"
  import Button from "$lib/components/ui/button/button.svelte"
  import RecordDetail from "./record-detail.svelte"
  import { queryParam, ssp } from "sveltekit-search-params"
  import { getTable, viewId } from "$lib/store/table.store"
  import { trpc } from "$lib/trpc/client"
  import { createQuery, useIsMutating, useQueryClient } from "@tanstack/svelte-query"
  import { RecordDO } from "@undb/table"
  import { derived } from "svelte/store"
  import Skeleton from "$lib/components/ui/skeleton/skeleton.svelte"
  import { cn } from "$lib/utils"
  import AuditList from "../audit/audit-list.svelte"
  import { HistoryIcon, LoaderCircleIcon } from "lucide-svelte"
  import { preferences } from "$lib/store/persisted.store"
  import { ScrollArea } from "$lib/components/ui/scroll-area"

  export let readonly = false

  const r = queryParam("r", ssp.string(), { pushHistory: false })

  const table = getTable()

  const record = createQuery(
    derived([table, r, preferences], ([$table, $recordId, $preferences]) => ({
      queryKey: [$recordId, "get", $preferences.showHiddenFields],
      queryFn: () => {
        return trpc.record.get.query({
          tableId: $table?.id.value,
          id: $recordId!,
          select: $preferences.showHiddenFields
            ? undefined
            : $table?.getOrderedVisibleFields($viewId).map((f) => f.id.value),
        })
      },
      enabled: !!$recordId,
    })),
  )

  const isUpdatingRecord = useIsMutating({
    mutationKey: ["updateRecord"],
  })

  const client = useQueryClient()

  $: recordDo = $record.data?.record ? RecordDO.fromJSON($table, $record.data?.record) : undefined

  let disabled = false
</script>

<Sheet.Root
  open={!!$r}
  onOpenChange={(open) => {
    if (!open) {
      r.set("")
    }
  }}
>
  <Sheet.Content
    class={cn(
      "sm:max-w-1/2 flex h-full w-1/2 flex-col gap-0 px-0 py-4 transition-all",
      $preferences.showAudit && "w-2/3",
    )}
    transitionConfig={{ duration: 50 }}
  >
    <Sheet.Header class="border-b px-6 pb-2">
      <Sheet.Title class="flex items-center justify-between">
        <span> Record Detail </span>
        <!-- <button disabled class="mr-6" on:click={() => ($preferences.showAudit = !$preferences.showAudit)}>
          <HistoryIcon class="text-muted-foreground h-4 w-4" />
        </button> -->
      </Sheet.Title>
    </Sheet.Header>

    <div class="flex-1 overflow-hidden">
      {#if $record.isLoading}
        <div class="space-y-4 px-6 py-4">
          <Skeleton class="h-10 w-full" />
          <Skeleton class="h-10 w-full" />
          <Skeleton class="h-10 w-full" />
          <Skeleton class="h-10 w-full" />
          <Skeleton class="h-10 w-full" />
        </div>
      {:else}
        <div class="grid h-full grid-cols-4 overflow-hidden">
          {#if recordDo}
            <div class={cn("overflow-hidden", $preferences.showAudit && $r ? "col-span-3" : "col-span-4")}>
              <div class="h-full w-full overflow-auto px-6">
                <RecordDetail
                  onSuccess={async () => {
                    $r = null
                    await client.invalidateQueries({ queryKey: ["records", $table.id.value] })
                  }}
                  {table}
                  {readonly}
                  record={recordDo}
                  bind:disabled
                />
              </div>
            </div>
          {/if}
          {#if $preferences.showAudit && $r}
            <div class="col-span-1 overflow-hidden border-l px-2">
              <ScrollArea class="h-full overflow-auto">
                <AuditList recordId={$r} />
              </ScrollArea>
            </div>
          {/if}
        </div>
      {/if}
    </div>

    {#if !readonly}
      <Sheet.Footer class="border-t px-6 pt-4">
        <Button variant="outline" type="button" on:click={() => ($r = null)}>Cancel</Button>
        <Button type="submit" form={`${$table.id.value}:updateRecord`} disabled={disabled || $isUpdatingRecord > 0}>
          {#if $isUpdatingRecord > 0}
            <LoaderCircleIcon class="mr-2 h-5 w-5 animate-spin" />
          {/if}
          Update
        </Button>
      </Sheet.Footer>
    {/if}
  </Sheet.Content>
</Sheet.Root>
